# Declaring Two variables 

var1 = 60
var2 = 3

# Perform and print various arithmatic operations
addition = var1 + var2 # Addition
subtraction = var1 - var2 # Subtraction
multiplication = var1 * var2  # Multiplication
division = var1 / var2 # Division
modulus = var1 % var2 # Modulus
exponentiation = var1 ** var2 # Exponentiation
floor_division = var1 // var2 # Floor Division

print("The two numbers are:", var1, "and", var2)
print("Addition:", var1, "+", var2, "=", addition)
print("Subtraction:", var1, "-", var2, "=", subtraction)
print("Multiplication:", var1, "*", var2, "=", multiplication)
print("Division:", var1, "/", var2, "=", division)
print("Modulus:", var1, "%", var2, "=", modulus)
print("Exponentiation:", var1, "^", var2, "=", exponentiation)
print("Floor Division:", var1, "//", var2, "=", floor_division)
