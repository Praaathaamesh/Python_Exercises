# a program to demonstrate the declaration and accessing dictionaries and its elements

# declare a dictionary
dict1 = {'emp1':22, 'emp2':55, 'emp3':33, 'emp4':88, 'emp5':75}

# Accessing the keys
print(dict1['emp1'])
print(dict1['emp4'])
print(dict1['emp3'])
print(dict1['emp5'])

# add a pair
dict1['emp6'] = 38
print(dict1)

# change a value of a key
dict1['emp2'] = 56
